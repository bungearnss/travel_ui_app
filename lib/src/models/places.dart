class Places {
  final String country;
  final String imageUrl;
  final String name;
  final String title;
  final double distance;
  final String owner;
  final String description;
  final double likes;
  final int comments;

  Places({
    required this.country,
    required this.imageUrl,
    required this.name,
    required this.title,
    required this.distance,
    required this.owner,
    required this.description,
    required this.likes,
    required this.comments,
  });
}
